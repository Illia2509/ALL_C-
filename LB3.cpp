#include <iostream>   // Підключаємо бібліотеку для вводу/виводу
#include <string.h>   // Підключаємо бібліотеку для роботи з C-style рядками

int main() {
    char teams[200] = "Динамо, Шахтар, Карпати, Зоря, Ворскла"; // Масив символів із назвами команд
    std::cout << "Рядок до заміни: " << teams << std::endl;     // Виводимо рядок до заміни

    char shachtar[] = "Шахтар";  // Окремий рядок із назвою "Шахтар"
    char dunamo[] = "Динамо";    // Окремий рядок із назвою "Динамо"
    
    // Пошук підрядків "Шахтар" та "Динамо" у рядку `teams`
    char* find_shachtar = strstr(teams, shachtar);  // Знаходимо вказівник на перше входження "Шахтар"
    char* find_dunamo = strstr(teams, dunamo);      // Знаходимо вказівник на перше входження "Динамо"

    // Перевіряємо, чи обидва слова знайдені
    if (find_shachtar && find_dunamo) {
        char temp[100];                     // Тимчасовий буфер для збереження тексту "Шахтар"
        strcpy(temp, shachtar);             // Копіюємо "Шахтар" у `temp`
        
        // Замінюємо "Шахтар" на "Динамо" у вихідному рядку
        strncpy(find_shachtar, dunamo, strlen(dunamo));
        
        // Замінюємо "Динамо" на "Шахтар" у вихідному рядку
        strncpy(find_dunamo, temp, strlen(temp));
    }

    // Виводимо рядок після виконання заміни
    std::cout << "Рядок після заміни: " << teams << std::endl;

    return 0;  // Завершення програми
}
